package com.devglan.model;

public class BowlerData {
    private String name;
    private String score;
    private String ballsBowled;
    private String economyRate;
    private String wicketsTaken;
    private String dotBalls;

    // Getters and Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }

    public String getBallsBowled() {
        return ballsBowled;
    }

    public void setBallsBowled(String ballsBowled) {
        this.ballsBowled = ballsBowled;
    }

    public String getEconomyRate() {
        return economyRate;
    }

    public void setEconomyRate(String economyRate) {
        this.economyRate = economyRate;
    }

	public String getWicketsTaken() {
		return wicketsTaken;
	}

	public void setWicketsTaken(String wicketsTaken) {
		this.wicketsTaken = wicketsTaken;
	}

	public String getDotBalls() {
		return dotBalls;
	}

	public void setDotBalls(String dotBalls) {
		this.dotBalls = dotBalls;
	}
	
	
    
    
}