package com.devglan.model;

public class VenueStats {
    private String matches;
    private String winBatFirst;
    private String winBowlFirst;
    private String avg1stInns;
    private String avg2ndInns;

    // Getters and Setters
    public String getMatches() {
        return matches;
    }

    public void setMatches(String matches) {
        this.matches = matches;
    }

    public String getWinBatFirst() {
        return winBatFirst;
    }

    public void setWinBatFirst(String winBatFirst) {
        this.winBatFirst = winBatFirst;
    }

    public String getWinBowlFirst() {
        return winBowlFirst;
    }

    public void setWinBowlFirst(String winBowlFirst) {
        this.winBowlFirst = winBowlFirst;
    }

    public String getAvg1stInns() {
        return avg1stInns;
    }

    public void setAvg1stInns(String avg1stInns) {
        this.avg1stInns = avg1stInns;
    }

    public String getAvg2ndInns() {
        return avg2ndInns;
    }

    public void setAvg2ndInns(String avg2ndInns) {
        this.avg2ndInns = avg2ndInns;
    }
}
